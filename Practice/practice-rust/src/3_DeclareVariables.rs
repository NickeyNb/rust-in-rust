// 3- Declare variables for an integer, a floating-point number, a boolean, a character, and a string. Print the values.

fn main() {
    let a:i32=10;
    let b:f32=1.2;
    let c:char='c';
    let d:String = String::from("dddd");
    let e:bool = true;

    println!("A is {}, B is {}, C is {}, D is {}, E is {}", a, b, c, d, e);
}